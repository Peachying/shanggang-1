CREATE DATABASE `shanggang` DEFAULT CHARACTER SET utf8 COLLATE utf8_general_ci;
USE shanggang;
DROP TABLE IF EXISTS project;

CREATE TABLE `project` (
	`PROJECT_ID` int(10) unsigned NOT NULL AUTO_INCREMENT,
	`PROJECT_NAME` varchar(100) NOT NULL,
	`DUMPINGAREA` varchar(100) NOT NULL,
	`SQUARE_VOLUME` varchar(100)  NOT NULL,
	`BEGIN_DATE` DATE,
	`END_DATE` DATE,
	`BOAT_NUM` INT(10) unsigned NOT NULL,
	PRIMARY KEY (`PROJECT_ID`)
)ENGINE=InnoDB AUTO_INCREMENT=11 DEFAULT CHARSET=utf8; 

alter table project add constraint FK_ID foreign key(DUMPINGAREA) REFERENCES dumping_area(area_id) ON DELETE CASCADE ON UPDATE CASCADE;

